package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println("Resultado da função Sin():", math.Sin(10.5))
	fmt.Println("Resultado da função Cos():", math.Cos(10.5))
	fmt.Println("Resultado da função Tan():", math.Tan(10.5))
	fmt.Println("Resultado da função Floor():", math.Floor(10.5))
	fmt.Println("Resultado da função Ceil():", math.Ceil(10.5))
	fmt.Println("Resultado da função Abs():", math.Abs(-10.5))
	fmt.Println("Resultado da função Sqrt():", math.Sqrt(10.5))
	fmt.Println("Resultado da função Pow():", math.Pow(2, 5))
}
